#Definimos una función a la cual le asignaremos una cantidad de números aleatorios y este devuelve un valor de pi
def pi(n):
  total_puntos = 0
  puntos_circulo = 0
  for i in range(0, n):
    x = random.random()
    y = random.random()
    if x**2 + y**2 < 1:
      puntos_circulo += 1
    total_puntos += 1
  return 4 * puntos_circulo / total_puntos

