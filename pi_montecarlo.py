epsilon = 0.0001 #Los pasos que daremos en busca del pi_00 acertado

pi_current = 3.0 #Valor inicial de pi

# Loop para generar candidatos de pi y calcular su R2
while pi_current < 3.2:
  pi_values_tested.append(pi_current) # Guardar el candidato actual de pi
  f_N = [np.abs(pi_values_tested[-1] - k) for k in pi_N] # Calcular f_00 para el actual candidato
  a, b = regresion_algebraica(N, f_N)
