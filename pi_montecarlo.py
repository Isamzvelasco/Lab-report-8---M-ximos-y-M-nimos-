#Definimos una función que nos aplique regresión exponencial a un conjunto de datos

def regresion_algebraica(x, y):
  Y = np.log(y)
  X = np.log(x)
  n = len(X)
  sum_x = sum(X)
  sum_y = sum(Y)
  sum_xy = sum([X[i]*Y[i] for i in range(n)])
  sum_x2 = sum([X[i]**2 for i in range(n)])
  a = (sum_y*sum_x2 - sum_x*sum_xy) / (n*sum_x2 - sum_x**2)
  b = (n*sum_xy - sum_x*sum_y) / (n*sum_x2 - sum_x**2)
  return a, b

#Definimos una función que nos calcule el coeficiente de determinación

def coeficiente_determinacion_algebraico(x, y, a, b):
    n = len(x)
    # 1. Calculamos el promedio de los datos reales de 'y'
    y_med = sum(y) / n

    deviation_y_pred = 0
    deviation_y = 0

    for i in range(n):
      # 2. Evaluamos usando la función exponencial REAL reconstruida: y = e^a * e^(b*x)
      #    que es equivalente a: np.exp(a + b * x[i])
      y_predicha = np.exp(a) * (x[i] ** b)

      # 3. Sumamos los residuos al cuadrado en la escala real
      deviation_y_pred += (y[i] - y_predicha) ** 2
      deviation_y += (y[i] - y_med) ** 2

    return 1 - (deviation_y_pred / deviation_y)

